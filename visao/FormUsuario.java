    private void jTablebuscaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablebuscaMouseClicked
        String nome_usu = "" + jTablebusca.getValueAt(jTablebusca.getSelectedRow(), 1);
        conex.conexao();
        conex.executasql("select * from usuarios where nome_usu='" + nome_usu + "'");
        try {
            conex.rs.first();
            jTextFieldid.setText(String.valueOf(conex.rs.getInt("cod_usu")));
            jTextFieldusuario.setText(conex.rs.getString("nome_usu"));
            jPasswordFieldsenha.setText(String.valueOf(conex.rs.getInt("senha_usu")));
            jPasswordFieldsenha2.setText(String.valueOf(conex.rs.getInt("senha_usu")));
            jComboBoxtipo.setSelectedItem(conex.rs.getString("tipo_usu"));
        } catch (SQLException ex) {
            Logger.getLogger(FormMedico.class.getName()).log(Level.SEVERE, null, ex);
        }
        jButtoneditar.setEnabled(true);
        jButtonexcluir.setEnabled(true);
        jButtoncancelar.setEnabled(true);
        jButtonnovo.setEnabled(false);
        conex.desconect();
    }//GEN-LAST:event_jTablebuscaMouseClicked

