    private void jButtonpesquizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonpesquizaActionPerformed
        jTextFieldusuario.setText("");
        jPasswordFieldsenha.setText("");
        jPasswordFieldsenha2.setText("");
        jTextFieldid.setText("");
        mod.setPesquiza(jTextFieldbusca.getText());
        BeansUsuario model = dao.buscaUsuario(mod);
        jTextFieldid.setText(String.valueOf(model.getCod()));
        jTextFieldusuario.setText(model.getNome());
        jPasswordFieldsenha.setText(String.valueOf(model.getSenha()));
        jComboBoxtipo.setSelectedItem(model.getTipo());
        jButtoneditar.setEnabled(!true);
        jButtonexcluir.setEnabled(!true);
        preencherTabela("select * from usuarios where nome_usu like '%" + mod.getPesquiza() + "%'");
    }//GEN-LAST:event_jButtonpesquizaActionPerformed

