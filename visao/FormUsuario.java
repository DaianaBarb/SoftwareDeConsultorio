    private void jButtonsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsalvarActionPerformed
        if (jTextFieldusuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo usuario precisa ser preenchido");
            jTextFieldusuario.requestFocus();
        } else if (jPasswordFieldsenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo senha precisa ser preenchido antes de salvar");
            jPasswordFieldsenha.requestFocus();
        } else if (!(jPasswordFieldsenha.getText()).equals(jPasswordFieldsenha2.getText())) {
            JOptionPane.showMessageDialog(null, "A senha e a confirmação da senha devem ser as mesmas");
        } else if (jPasswordFieldsenha2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo confirmação de senha deve ser preenchido");
        } else if (flag == 1) {
            mod.setNome(jTextFieldusuario.getText());
            mod.setSenha((jPasswordFieldsenha.getText()));
            mod.setTipo((String) jComboBoxtipo.getSelectedItem());
            dao.salvar(mod);
            jTextFieldusuario.setText("");
            jPasswordFieldsenha.setText("");
            jPasswordFieldsenha2.setText("");
            jTextFieldusuario.setEnabled(false);
            jPasswordFieldsenha.setEnabled(false);
            jPasswordFieldsenha2.setEnabled(false);
            jComboBoxtipo.setEnabled(false);
            jButtonsalvar.setEnabled(false);
            jTextFieldid.setText("");
            jTextFieldbusca.setText("");
            jButtoncancelar.setEnabled(false);
            jTextFieldbusca.setEnabled(true);
            jButtonpesquiza.setEnabled(true);
            jButtonnovo.setEnabled(true);
            jButtoneditar.setEnabled(false);

        } else {
            mod.setCod(Integer.parseInt(jTextFieldid.getText()));
            mod.setNome(jTextFieldusuario.getText());
            mod.setTipo((String) (jComboBoxtipo.getSelectedItem()));
            mod.setSenha((String) (jPasswordFieldsenha.getText()));
            dao.editar(mod);
            jTextFieldusuario.setEnabled(false);
            jPasswordFieldsenha.setEnabled(false);
            jPasswordFieldsenha2.setEnabled(false);
            jComboBoxtipo.setEnabled(false);
            jButtonsalvar.setEnabled(false);
            jTextFieldusuario.setText("");
            jPasswordFieldsenha2.setText("");
            jPasswordFieldsenha.setText("");
            jButtonnovo.setEnabled(true);
            jButtoncancelar.setEnabled(false);
            jTextFieldid.setText("");
            jTextFieldbusca.setText("");
            jButtoneditar.setEnabled(false);
        }
    }//GEN-LAST:event_jButtonsalvarActionPerformed

